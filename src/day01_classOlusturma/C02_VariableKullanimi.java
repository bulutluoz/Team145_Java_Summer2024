package day01_classOlusturma;

public class C02_VariableKullanimi {

    public static void main(String[] args) {


        int sayi  = 20 ;





        System.out.println(sayi); // 20
        // sout icerisinde cift tirnak kullanildiginda
        // "" arasindaki metin oldugu gibi yazdirilir
        // "" yoksa, java yazdiracagi seyin
        // bir variable'in degeri oldugunu bilir


        System.out.println("sayi"); // sayi

        sayi = sayi + 5 ;

        System.out.println(sayi); // 25

    }
}
