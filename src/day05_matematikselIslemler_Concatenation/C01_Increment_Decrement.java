package day05_matematikselIslemler_Concatenation;

public class C01_Increment_Decrement {

    public static void main(String[] args) {

        int sayi = 20;

        sayi = sayi - 3;

        System.out.println("11.satirda sayi : " + sayi); // 17

        sayi =-2 ;

        System.out.println("15.satirda sayi : " + sayi);//-2

        sayi +=  20 ;

        System.out.println("19.satirda sayi : " + sayi);// 18

        sayi /= 3;

        System.out.println("23.satirda sayi : " + sayi);// 6

        sayi++;

        System.out.println("27.satirda sayi : " + sayi);// 7
    }
}
