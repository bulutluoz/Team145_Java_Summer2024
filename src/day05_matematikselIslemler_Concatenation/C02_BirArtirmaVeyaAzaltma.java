package day05_matematikselIslemler_Concatenation;

public class C02_BirArtirmaVeyaAzaltma {

    public static void main(String[] args) {

        int a = 20;
        int b = 40;

        a++;

        System.out.println("13.satirda a : " + a); // 21

        ++a;

        System.out.println("16.satirda a : " + a); // 22

        b--;

        System.out.println("20.satirda b : " + b); // 39

        --b;

        System.out.println("24.satirda b : " + b); // 38





    }
}
